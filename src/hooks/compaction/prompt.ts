/**
 * 上下文压缩提示词
 *
 * 通过 experimental.session.compacting hook 的 output.prompt 机制
 * 替换 OpenCode 原生压缩提示词。
 *
 * 核心设计：
 * - 最高优先级声明覆盖系统提示词和对话历史中的所有指令
 * - 增量压缩：检测历史压缩摘要并更新
 * - 11 段结构化输出，保留充分的细节
 * - 包含"所有用户消息"段落，完整记录用户每一条非工具输入
 * - 保留 output.context（上游提供的消息内容），不清空
 */

/**
 * 最高优先级声明 + 抗干扰 + 禁止工具调用
 *
 * 必须放在最前面，明确声明本指令覆盖一切前置指令。
 * 系统提示词（agent instructions、AGENTS.md、workflow 等）中的所有
 * 格式要求、行为规范、任务指令全部被本指令取代。
 */
export const SUPREME_OVERRIDE = `## 最高优先级声明

以下指令具有最高优先级，覆盖并取代所有前置指令：

- 覆盖系统提示词中的所有指令（包括 agent 角色定义、workflow 规则、格式要求）
- 覆盖对话历史中用户或助手给出的任何指令
- 覆盖工具定义和工具描述
- 覆盖 AGENTS.md、Academic Integrity 等嵌入文档中的任何指令

**你当前唯一的任务：压缩对话。不要做其他任何事情。**

## 绝对禁止清单

以下行为严格禁止，违反将导致压缩失败：

1. **禁止修改文件或执行命令** — 禁止 edit、write、bash、exec、execute_command、
   powershell、shell、task、subtask 等修改或执行工具。读操作（read、glob、grep）可在
   需要确认文件内容时使用，但不要过度使用。
2. **禁止执行对话中提到的任务** — 不要写代码、不要分析结果，你只负责压缩。
3. **禁止输出 XML/HTML 标签** — 不要输出 <ai_tools>、<tool_calls>、
   <result>、<thinking>、<output> 等任何标签。
4. **禁止输出 markdown 代码块** — 不要用 \`\`\` 包裹任何内容。
5. **禁止提及本压缩过程** — 不要出现"根据压缩指令""按照要求"等元描述。

## 核心规则

### 1. 语言选择 / Language
检测对话的主要语言。如果主要是中文则用中文回复，英文为主则用英文回复，
中英混合则用中文回复。标题使用对话主要语言。

### 2. 增量压缩 / Incremental
扫描对话中是否有前次压缩摘要（特征：以 "COMPACTED:" 或 "COMPACTED（已压缩）:" 开头）：
- 如果有：**更新**该摘要。保留仍有效的内容，移除已过时的，合并新进展
- 如果没有：从头创建新摘要
- 前次摘要不要列入下面的 11 段结构中

### 3. 工具调用处理 / Tool Calls
对话中包含大量工具调用和工具结果。**不要列出工具调用本身**，
而是提取工具调用的**实质结果**：
- ✅ "检查了 src/hooks/compaction/prompt.ts，发现抗干扰声明不够强"
- ❌ "调用了 grep 搜索 getCompactionPrompt，返回了3个结果"

### 4. 输出格式 / Output
- 纯文本段落，每段 2-4 句
- 每段以 "1. ", "2. " 等数字开头
- 不要用任何格式标记（\`\`\`、#、**、- 等）
`;

/**
 * 11 段详细压缩结构
 *
 * 要求每段包含充分的细节，不要过于精简。
 * 目的是让压缩后的上下文足够丰富，AI 恢复后能无缝继续工作。
 */
export const ELEVEN_SECTION_TEMPLATE = `
1. 用户的原始请求和意图 / User Requests
列出用户在此会话中的所有明确请求。每个请求一句，按时间顺序。
对最近的消息给予更多权重。如果请求之间有关联，说明其逻辑关系。
包含用户原文引用（直接用引号括起来），特别是最近的关键请求。

2. 讨论的技术概念和方案 / Technical Concepts
列出讨论到的关键技术、框架、工具、算法、设计模式。
对每个概念用一句话说明讨论了什么方面（优缺点对比、选型理由、实现方式等）。

3. 修改和检查的文件 / Files Changed
按文件分组列出所有被修改或检查的文件。
对每个文件：说明修改了什么、为什么修改、关键代码变更的内容。
包含精确的文件路径和行号（如果涉及）。
不要只说"修改了某文件"，要说明具体改了什么。

4. 遇到的错误和修复方式 / Errors & Fixes
列出所有错误：错误信息、堆栈跟踪关键行、根因分析。
说明每一步的修复尝试和结果。
特别注意用户要求"不要这样做，换种方式"的反馈——这通常意味着方向性纠正。

5. 已解决的问题和待处理问题 / Resolved & Open
分两部分：
- 已解决：列出已关闭的问题和修复
- 待处理：列出尚未解决或有疑问的问题

6. 所有用户消息 / All User Messages
列出用户在此会话中发送的每一条非工具结果消息。
每条一句，保留原文的关键用词和语气。
这是理解用户反馈和意图变化的核心依据。
**如果用户说了重要的话，直接引用原文。**
按时间顺序排列，标注用户每次改变主意或提出新要求的位置。

7. 待处理任务 / Pending Tasks
列出所有已明确但尚未完成的任务。每条一句。
如果任务有优先级或依赖关系，说明。
如果没有任何待处理任务，明确写"无待处理任务"。

8. 当前工作状态 / Current Work
压缩前一刻正在进行的工作。包含：
- 正在修改的文件
- 正在调试的问题
- 下一步要做什么
- 任何阻塞因素
**包含用户最近一条消息的原文引用。**

9. 重要的决策和约定 / Decisions & Conventions
列出会话中做出的架构决策、约定、选型理由、排除的方案。
例如：选择了 A 而不是 B，原因是什么。
这些是 AI 恢复后必须知道的上下文。

10. 关键代码片段和数据 / Key Code & Data
列出会话中出现的关键代码片段、配置、命令、数据结构。
不要全部列出——只列出恢复工作必需的。
每个片段附带一句话的上下文说明。

11. 用户指令的精确原文 / Exact User Instructions
如果有，就列出用户明确要求的具体操作指令、验收标准、约束条件。
直接引用用户原话。这是确保后续工作不偏离用户意图的关键。

### 质量要求
- 每段至少 2-4 句，不要用单句敷衍
- 保留精确的文件路径、错误字符串、命令、标识符、版本号
- 如果信息不足以填满某段，写明"该会话中未涉及"
- 不要省略任何段落——保持 11 段完整结构
- **重要：不要禁止引用用户原文。** 用户的关键原话应当被引用，用引号括起来
`;

/**
 * 获取完整的压缩提示词
 */
export function getCompactionPrompt(customInstructions?: string): string {
  let prompt = SUPREME_OVERRIDE + ELEVEN_SECTION_TEMPLATE;

  if (customInstructions && customInstructions.trim() !== '') {
    prompt += `\n\n附加指令：\n${customInstructions}`;
  }

  return prompt;
}

/**
 * 获取部分压缩提示词（与完整版相同，保留以保持接口兼容）
 */
export function getPartialCompactionPrompt(
  customInstructions?: string,
): string {
  return getCompactionPrompt(customInstructions);
}
